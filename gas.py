def check_gas_levels(hc, o2, co2, co, h2s):
    # 인화성가스 (HC)
    if hc > 25:
        print("위험: 인화성가스(HC) 수치가 안전 기준을 초과했습니다.")
    elif hc >= 21:
        print("경고: 인화성가스(HC) 수치가 85% 기준에 도달했습니다.")

    # 산소 (O2)
    if o2 < 18 or o2 > 23.5:
        print("위험: 산소(O2) 수치가 안전 기준을 초과했습니다.")
    elif o2 <= 20:
        print("경고: 산소(O2) 수치가 85% 기준에 도달했습니다.")

    # 탄산가스 (CO2)
    if co2 > 1.5:
        print("위험: 탄산가스(CO2) 수치가 안전 기준을 초과했습니다.")
    elif co2 >= 1.3:
        print("경고: 탄산가스(CO2) 수치가 85% 기준에 도달했습니다.")

    # 일산화탄소 (CO)
    if co > 30:
        print("위험: 일산화탄소(CO) 수치가 안전 기준을 초과했습니다.")
    elif co >= 25:
        print("경고: 일산화탄소(CO) 수치가 85% 기준에 도달했습니다.")

    # 황화수소 (H2S)
    if h2s > 10:
        print("위험: 황화수소(H2S) 수치가 안전 기준을 초과했습니다.")
    elif h2s >= 8.5:
        print("경고: 황화수소(H2S) 수치가 85% 기준에 도달했습니다.")

# 예시 입력
check_gas_levels(100, 19, 1.4, 28, 9)